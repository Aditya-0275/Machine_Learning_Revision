import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.preprocessing import StandardScaler
from imblearn.over_sampling import RandomOverSampler

cols = ['fLength', 'fWidth', 'fSize', 'fConc', 'fConc1', 'fAsym', 'fM3Long', 'fM3Trans', 'fAlpha', 'fDist', 'class']
df = pd.read_csv('magic04.data', names=cols)
df.head()

print("Unique values in 'class' before conversion:")
print(df['class'].unique())

"""The output above will show the exact values. If there are extra spaces, for example, ` 'g'` instead of ` 'g'`, the direct comparison `df['class'] == "g"` will evaluate to `False`. To fix this, we can strip any whitespace before comparison."""

df['class'] = (df['class'] == "g").astype(int)
df.head()

for label in cols[:-1]:
  plt.hist(df[df["class"] == 1][label], color='blue', alpha=0.7, density=True, label='gamma')
  plt.hist(df[df["class"] == 0][label], color='red', alpha=0.7, density=True, label='hadron')
  plt.title(f"Distribution of {label}")
  plt.ylabel("Probability")
  plt.xlabel(label)
